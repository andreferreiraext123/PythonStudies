# Lógica do operador And.

# Atribuindo valor as variaveis
salario = 100
idade = 18

# Verificacao dos operandos das variaveis
verificar = salario > 1000 and idade >= 18

# Imprimindo resultado dos Operandos sobre operador lógico And
print(verificar)

#   O operador lógico And, somente devolverá resultado True somente se todos os operados forem verdadeiros!
#   Ou seja, as lógicas devem ser aplicadas visando o resultado verdadeiro a todos os operandos.
