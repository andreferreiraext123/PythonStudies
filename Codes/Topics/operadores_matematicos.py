# Operadores Matematicos

# Adiçao (n1+n2)
n1 = 10+5

# Suntracao (n1-n2)
n2 = 10-5

# Divisao (n1/n2)
n3 = 10/5

# Multiplicacao (n1*n2)
n4 = 10*5

# Divisao inteira (n1//n2)
n5 = 10//5

# Elevar número ao expoente (n1**n2)
n6 = 10**5

# Emrprimindo resultados 
print(n1)
print(n2)
print(n3)
print(n4)
print(n5)
print(n6)