# Atribuindo valores as variveis
idade = 7.2
peso = 22.3

# Buscando tipos diferentes de dados?
inteiro = "%d" % idade # Puxa um número inteiro
float = "%f" % peso # Puxa um número float decimal
icognita = "%1.1f" % idade # Adiciona o zeros a esquerda do número (Para alterar o tipo do número basta alterar a letra)

# Imprimindo resultados
print(inteiro)
print(float)
print(icognita)


    # Letra d representa números inteiros. %d
    # Letra f representa número floats. %f
    # Letra s representa valores strings. %s
