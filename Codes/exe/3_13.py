# Escreva um programa que converta uma temperatura digitada em C em F. 

# Armazenando Inputs
celcius = int(input("Digite aqui a temperatura em C: "))

# Conversao da temperatura 
Fahrenheit = (9*celcius) /5 + 32

# Imprimindo resultados
print(f"{celcius}.C convertido para Fahrenheit, {Fahrenheit}.F")