# Peça dois números e imprima o resultado da soma deles.

# Iniciando Inputs
number_1 = int(input("Digite aqui um número: "))
number_2 = int(input("Digite aqui um número: "))

# Soma dos valores de input
soma = number_1 + number_2

# Impreessao do resultado da soma
print(f"A soma entre {number_1} e {number_2} é igual a {soma}")

