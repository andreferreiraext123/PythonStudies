# Escreva um programa que leia um valor em metros e o exiba convertido em milímetros

# Input do valor em metros
metros = float(input("Digite aqui a medida em metros: "))

# Conversao do valor em Metros para Milímetros
conversao_milimetros = metros * 1000

# Empreessao do valor já converito 
print(f"A medida {metros} metros convertido para milimetros é {conversao_milimetros:.2f} militros.")