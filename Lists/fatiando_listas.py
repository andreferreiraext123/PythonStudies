# Fatiando listas em python

#Storing list
lista  = [1,2,3,4,5]

print(lista[0:5]) # Os elementos entre a posicao 0 e 4 (O cinco fica de fora)
print(lista[:5]) # Os primeiros 5 elementos
print(lista[:-1]) # Todos os elementos menos o elemento do último indice
print(lista[1:3]) # Elementos 1 e 2, (o 3 é excluído)
print(lista[1:4]) # Elementos 1,2 3, (o 4 é excluído)
print(lista[:3]) # Elementos até a posiçao 3
print(lista[-1]) # Último elemento da lista
print(lista[-2]) # Penúltimo elemento da lista

