# Criando uma lista simples em python
even_numbers = [2,4,6,8]
print(even_numbers)
print(f"Indice 0:   {even_numbers[0]}")
print(f"Indice 3:   {even_numbers[3]}")

even_numbers[0] = 10
print(f"Novo elemento no indice 0:  {even_numbers[0]}")




