# Sintaxe da funcao range utlizando o for

for element in range(10): # Parametro: Stop
    print(element)

print()

for element in range(3, 10): # Parametro: Start and Stop
    print(element)

print()

for element in range(0,10,2): # Parametro: Start, stop, step
    print(element)

# Funcao Range cria uma sequencia númerica, podendo ser criada com os seguintes parametros, start, stop & step. Ou seja, qual indice irá iniciar, qual indice ira terminar, de quanto em quanto será pulados os indices do elemento, (ex: 2 em 2, 4 em 4(Setps))